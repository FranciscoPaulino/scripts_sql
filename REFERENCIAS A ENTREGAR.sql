JOIN VENDAS B ON B.PEDIDO=A.PEDIDO
WHERE QTDE_ENTREGAR>0 AND A.ENTREGA>='20141101' AND A.LIMITE_ENTREGA <='20141130' AND
RTRIM(PRODUTO)+COR_PRODUTO IN (
'40705PT0124',
'40907PT0124',
'421507.1RM210',
'42256TG 450',
'42260BI 561',
'42260.2BI 561',
'42468NN315',
'42469MK 62',
'42472VL230',
'42911CJ0299',
'43168AR0094',
'45352LL 215',
'45352PK1074',
'45457AR0094',
'47457AR0094',
'51260MK 62',
'52902MS227') 
GROUP BY A.PEDIDO,B.CLIENTE_ATACADO,B.TOT_QTDE_ORIGINAL
ORDER BY A.PEDIDO
