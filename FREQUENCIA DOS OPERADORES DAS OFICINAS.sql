PRODUCAO_RECURSOS.RECURSO_PRODUTIVO
FROM LDR_METAS_OFICINA_GRUPO_NOVA
JOIN PRODUCAO_RECURSOS ON PRODUCAO_RECURSOS.DESC_RECURSO=LDR_METAS_OFICINA_GRUPO_NOVA.DESC_RECURSO
JOIN napsi_tbl_relacao_oficinas ON napsi_tbl_relacao_oficinas.desc_recurso_produtivo=PRODUCAO_RECURSOS.RECURSO_PRODUTIVO
where LDR_METAS_OFICINA_GRUPO_NOVA.DESC_SETOR_PRODUCAO IN('OFICINA ATHENA','OFICINA FIORUCCI CONFECCOES','OFICINA AFRODITE CANINDE') AND CONVERT(CHAR(10),DATA_PRODUCAO,103) = CONVERT(CHAR(10),GETDATE(),103) --AND napsi_tbl_relacao_oficinas.qtde_oper_dia>0
ORDER BY DESC_SETOR_PRODUCAO,LDR_METAS_OFICINA_GRUPO_NOVA.DESC_RECURSO


