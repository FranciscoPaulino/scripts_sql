UPDATE A
SET A.QTDE_FALTAS=napsi_tbl_relacao_oficinas.qtde_faltas_dia, A.QTDE_OPERADOR=napsi_tbl_relacao_oficinas.qtde_oper_dia,A.QTDE_FERIAS=napsi_tbl_relacao_oficinas.qtde_oper_afastados
FROM LDR_METAS_OFICINA_GRUPO_NOVA A
JOIN PRODUCAO_RECURSOS ON PRODUCAO_RECURSOS.DESC_RECURSO=A.DESC_RECURSO
JOIN napsi_tbl_relacao_oficinas ON napsi_tbl_relacao_oficinas.desc_recurso_produtivo=PRODUCAO_RECURSOS.RECURSO_PRODUTIVO
WHERE A.DESC_SETOR_PRODUCAO IN('OFICINA ATHENA','OFICINA FIORUCCI CONFECCOES','OFICINA AFRODITE CANINDE') AND CONVERT(CHAR(10),DATA_PRODUCAO,103) = CONVERT(CHAR(10),GETDATE(),103) AND napsi_tbl_relacao_oficinas.qtde_oper_dia>0

